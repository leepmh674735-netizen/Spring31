-- 1. 테이블 삭제 (초기화용)
DROP TABLE ATTENDANCE CASCADE CONSTRAINTS;
DROP TABLE PAYMENT CASCADE CONSTRAINTS;
DROP TABLE MEMBER CASCADE CONSTRAINTS;

-- 2. MEMBER (회원) 테이블 생성
CREATE TABLE MEMBER (
    MEMBER_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME              VARCHAR2(50) NOT NULL,
    GENDER            VARCHAR2(10) CONSTRAINT CK_MEMBER_GENDER CHECK (GENDER IN ('M', 'F')),
    BIRTH_DATE        DATE,
    PHONE             VARCHAR2(20),
    JOIN_DATE         DATE DEFAULT SYSDATE NOT NULL,
    MEMBERSHIP_TYPE   VARCHAR2(20) CONSTRAINT CK_MEMBERSHIP_TYPE CHECK (MEMBERSHIP_TYPE IN ('1 Month', '3 Months', '6 Months', '12 Months')),
    IS_LOCKER_USED    NUMBER(1) DEFAULT 0 CONSTRAINT CK_LOCKER CHECK (IS_LOCKER_USED IN (0, 1)),
    IS_CLOTHES_USED   NUMBER(1) DEFAULT 0 CONSTRAINT CK_CLOTHES CHECK (IS_CLOTHES_USED IN (0, 1)),
    STATUS            VARCHAR2(20) DEFAULT 'ACTIVE' CONSTRAINT CK_MEMBER_STATUS CHECK (STATUS IN ('ACTIVE', 'FROZEN', 'EXPIRED'))
);

COMMENT ON TABLE MEMBER IS '헬스장 회원 마스터 테이블';
COMMENT ON COLUMN MEMBER.MEMBER_ID IS '회원 고유 식별번호 (자동증가)';
COMMENT ON COLUMN MEMBER.IS_LOCKER_USED IS '락커 이용 여부 (0:미사용, 1:사용)';

-- 3. ATTENDANCE (출석 일지) 테이블 생성
CREATE TABLE ATTENDANCE (
    ATTENDANCE_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEMBER_ID         NUMBER NOT NULL,
    CHECK_IN_TIME     TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_ATTENDANCE_MEMBER FOREIGN KEY (MEMBER_ID) 
        REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE
);

COMMENT ON TABLE ATTENDANCE IS '회원 일일 출석 체크인 기록';

-- 4. PAYMENT (결제 이력) 테이블 생성
CREATE TABLE PAYMENT (
    PAYMENT_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEMBER_ID         NUMBER NOT NULL,
    PAYMENT_DATE      DATE DEFAULT SYSDATE NOT NULL,
    AMOUNT            NUMBER NOT NULL,
    MONTHS_REGISTERED NUMBER NOT NULL,
    IS_RENEWED        NUMBER(1) DEFAULT 0 CONSTRAINT CK_PAYMENT_RENEW CHECK (IS_RENEWED IN (0, 1)),
    CONSTRAINT FK_PAYMENT_MEMBER FOREIGN KEY (MEMBER_ID) 
        REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE
);

-- 5. 인덱스 생성 (조회 성능 최적화)
-- 회원별 출석 및 결제 조회가 잦으므로 외래키 열에 인덱스를 부여합니다.
CREATE INDEX IX_ATTENDANCE_MEMBER ON ATTENDANCE(MEMBER_ID);
CREATE INDEX IX_ATTENDANCE_TIME ON ATTENDANCE(CHECK_IN_TIME);
CREATE INDEX IX_PAYMENT_MEMBER ON PAYMENT(MEMBER_ID);

