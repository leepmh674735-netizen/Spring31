-- 7. PL/SQL 프로시저 생성: 회원별 이탈 예측용 특성값(Feature) 집계 추출
-- 특정 회원의 [미방문일수, 최근 30일 출석수, 주간 출석수]를 한 번에 조회합니다.
CREATE OR REPLACE PROCEDURE PR_GET_CHURN_FEATURES (
    P_MEMBER_ID IN NUMBER,
    O_RECENCY OUT NUMBER,
    O_FREQ_MONTHLY OUT NUMBER,
    O_FREQ_WEEKLY OUT NUMBER
) AS
    V_LAST_VISIT TIMESTAMP;
BEGIN
    -- 1. 마지막 방문 날짜 조회
    SELECT MAX(CHECK_IN_TIME)
    INTO V_LAST_VISIT
    FROM ATTENDANCE
    WHERE MEMBER_ID = P_MEMBER_ID;

    IF V_LAST_VISIT IS NULL THEN
        -- 출석 기록이 전혀 없을 시 기본값 세팅
        O_RECENCY := 999;
    ELSE
        -- 현재일과 마지막 방문일의 날짜 차 계산 (Oracle DATE 연산 활용)
        O_RECENCY := ROUND(SYSDATE - CAST(V_LAST_VISIT AS DATE));
    END IF;

    -- 2. 최근 30일 출석 일수 집계
    SELECT COUNT(DISTINCT TRUNC(CHECK_IN_TIME))
    INTO O_FREQ_MONTHLY
    FROM ATTENDANCE
    WHERE MEMBER_ID = P_MEMBER_ID
      AND CHECK_IN_TIME >= SYSTIMESTAMP - INTERVAL '30' DAY;

    -- 3. 최근 7일 출석 일수 집계
    SELECT COUNT(DISTINCT TRUNC(CHECK_IN_TIME))
    INTO O_FREQ_WEEKLY
    FROM ATTENDANCE
    WHERE MEMBER_ID = P_MEMBER_ID
      AND CHECK_IN_TIME >= SYSTIMESTAMP - INTERVAL '7' DAY;
END;
